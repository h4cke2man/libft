#include "libft.h"

char	*ft_strcat(char *s1, char *s2)
{
	int	i;
	int j;

	i = ft_strlen(s1);
	j = -1;
	while (s2[++j])
		s1[i + j] = s2[j];
	s1[i + j] = '\0';
	return (s1);
}
